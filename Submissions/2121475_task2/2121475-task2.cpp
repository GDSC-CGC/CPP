#include <iostream>
using namespace std;

int main(){
    // Using sizeof function to get the memory size
    cout<<"Memory size of int: "<<sizeof(int)<<endl;
    cout<<"Memory size of char: "<<sizeof(char)<<endl;
    cout<<"Memory size of float: "<<sizeof(float)<<endl;
    cout<<"Memory size of bool: "<<sizeof(bool)<<endl;
    cout<<"Memory size of double: "<<sizeof(double)<<endl;
    return 0;
}
